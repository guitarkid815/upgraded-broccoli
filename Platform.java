
public class Platform{
    private int x;
    private int y;
    private int length;
    private int height;
    public Platform(int nX, int nY, int nLength, int nHeight){
        x=nX;
        y=nY;
        length=nLength;
        height=nHeight;
    }
    public int getX(){
        return x;
    }
    public int getY(){
        return y;
    }
    public int getLength(){
        return length;
    }
    public int getHeight(){
        return height;
    }
    
}